const COOKIE_NAME = "multica-locale";
const STORAGE_KEY = "multica-locale";
const COOKIE_MAX_AGE = 60 * 60 * 24 * 365;

function formatMessage(template: string, values?: TranslationValues): string {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

function getMessage(locale: Locale, key: string): string {
  return messages[locale][key] ?? messages.en[key] ?? key;
}

function detectLocale(): Locale {
  if (typeof document !== "undefined") {
    const match = document.cookie.match(/(?:^|;\s*)multica-locale=(\w+)/);
    const cookieLocale = match?.[1];
    if (cookieLocale && locales.includes(cookieLocale as Locale)) return cookieLocale as Locale;
  }

  if (typeof window !== "undefined") {
    try {
      const stored = window.localStorage.getItem(STORAGE_KEY);
      if (stored && locales.includes(stored as Locale)) return stored as Locale;
    } catch {
      // Ignore storage access failures.
    }
  }

  if (typeof navigator !== "undefined") {
    const language = navigator.language.toLowerCase();
    if (language.startsWith("zh")) return "zh";
    if (language.startsWith("ja")) return "ja";
    if (language.startsWith("ko")) return "ko";
    if (language.startsWith("es")) return "es";
    if (language.startsWith("fr")) return "fr";
    if (language.startsWith("de")) return "de";
    return "en";
  }

  return "en";
}

function persistLocale(locale: Locale) {
  if (typeof document !== "undefined") {
    document.documentElement.lang = locale;
    document.cookie = `${COOKIE_NAME}=${locale}; path=/; max-age=${COOKIE_MAX_AGE}; SameSite=Lax`;
  }

  if (typeof window !== "undefined") {
    try {
      window.localStorage.setItem(STORAGE_KEY, locale);
    } catch {
      // Ignore storage access failures.
    }
  }
}

type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, values?: TranslationValues) => string;
};

const defaultValue: I18nContextValue = {
  locale: "en",
  setLocale: () => {},
  t: (key, values) => formatMessage(getMessage("en", key), values),
};

const I18nContext = createContext<I18nContextValue>(defaultValue);

export function I18nProvider({
  children,
  initialLocale,
}: {
  children: React.ReactNode;
  initialLocale?: Locale;
}) {
  const [locale, setLocaleState] = useState<Locale>(() => initialLocale ?? detectLocale());

  useEffect(() => {
    persistLocale(locale);
  }, [locale]);

  const setLocale = useCallback((nextLocale: Locale) => {
    setLocaleState(nextLocale);
  }, []);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, values) => formatMessage(getMessage(locale, key), values),
    }),
    [locale, setLocale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
